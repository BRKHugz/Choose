package projeto_choose;

public class Cadastro {

    private String nome;
    private String cpf;
    private String regiao;
    private int idade;
    private String email;

    public String getNome() {
        return this.nome;
    }
    public String getCpf() {
        return this.cpf;
    }
    public String getRegiao() {
        return this.regiao;
    }
    public int getIdade() {
        return this.idade;
    }
    public String getEmail() {
        return this.email;
    } 
}



